package casting_concept;

public class Casting_Primitive {

    public static void main(String[] args) {
        //Type casting: converting one data type to another data type is called type casting.
        //for primitive data type--implicit(small to large) and explicit(large to small).
        double d =10.3;
        int i = (int) d;
        System.out.println(i);
    }
}
